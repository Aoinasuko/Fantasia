def tr_enum(domain: str, value: Any, language: Any = "ja", fallback: str | None = None) -> str:
    lang = language_code(language)
    key = str(value or "").strip()
    if not key:
        return fallback or ""
    table = ENUM_TEXT.get(lang, ENUM_TEXT["ja"]).get(domain, {})
    if key in table:
        return table[key]
    default_table = ENUM_TEXT["en"].get(domain, {})
    if key in default_table:
        return default_table[key]
    return fallback if fallback is not None else key


def tr_enum_format(domain: str, value: Any, language: Any = "ja", fallback: str | None = None, **values: Any) -> str:
    template = tr_enum(domain, value, language, fallback)
    try:
        return template.format(**values)
    except (KeyError, IndexError, ValueError):
        return template
